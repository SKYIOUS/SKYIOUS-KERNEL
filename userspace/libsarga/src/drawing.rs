use crate::color::Color;

#[derive(Clone)]
pub struct Backbuffer {
    pub width: usize,
    pub height: usize,
    pub stride: usize,
    pub pixels: alloc::vec::Vec<u32>,
}

impl Backbuffer {
    pub fn new(width: usize, height: usize) -> Self {
        let size = width * height;
        let mut pixels = alloc::vec::Vec::with_capacity(size);
        pixels.resize(size, 0);
        Backbuffer { width, height, stride: width, pixels }
    }

    pub fn clear(&mut self, color: Color) {
        self.fill_rect(0, 0, self.width, self.height, color);
    }

    pub fn set_px(&mut self, x: usize, y: usize, color: Color) {
        if x < self.width && y < self.height {
            self.pixels[y * self.stride + x] = color.0;
        }
    }

    pub fn fill_rect(&mut self, x: usize, y: usize, w: usize, h: usize, color: Color) {
        let x0 = x;
        let x1 = core::cmp::min(x + w, self.width);
        let y1 = core::cmp::min(y + h, self.height);
        for row in y..y1 {
            let start = row * self.stride + x0;
            let end = row * self.stride + x1;
            self.pixels[start..end].fill(color.0);
        }
    }

    pub fn draw_rect(&mut self, x: usize, y: usize, w: usize, h: usize, color: Color) {
        self.fill_rect(x, y, w, 1, color);
        self.fill_rect(x, y + h - 1, w, 1, color);
        self.fill_rect(x, y, 1, h, color);
        self.fill_rect(x + w - 1, y, 1, h, color);
    }

    pub fn draw_char(&mut self, x: usize, y: usize, ch: u8, fg: Color, bg: Color) {
        if ch < 32 || ch > 127 {
            return;
        }
        let glyph = FONT_BASIC_8X8[(ch - 32) as usize];
        let mut row;
        for row_y in 0..8 {
            row = glyph[row_y];
            for col in 0..8 {
                if row & (0x80 >> col) != 0 {
                    self.set_px(x + col, y + row_y, fg);
                } else {
                    self.set_px(x + col, y + row_y, bg);
                }
            }
        }
    }

    pub fn draw_text(&mut self, x: usize, y: usize, text: &str, fg: Color, bg: Color) {
        let mut cx = x;
        for &b in text.as_bytes() {
            if b == b'\n' {
                cx = x;
                continue;
            }
            self.draw_char(cx, y, b, fg, bg);
            cx += 8;
            if cx + 8 > self.width {
                break;
            }
        }
    }

    pub fn draw_char_bgless(&mut self, x: usize, y: usize, ch: u8, fg: Color) {
        if ch < 32 || ch > 127 {
            return;
        }
        let glyph = FONT_BASIC_8X8[(ch - 32) as usize];
        for row_y in 0..8 {
            let row = glyph[row_y];
            for col in 0..8 {
                if row & (0x80 >> col) != 0 {
                    self.set_px(x + col, y + row_y, fg);
                }
            }
        }
    }

    pub fn draw_text_bgless(&mut self, x: usize, y: usize, text: &str, fg: Color) {
        let mut cx = x;
        for &b in text.as_bytes() {
            if b == b'\n' {
                cx = x;
                continue;
            }
            self.draw_char_bgless(cx, y, b, fg);
            cx += 8;
            if cx + 8 > self.width {
                break;
            }
        }
    }

    pub fn as_bytes(&self) -> &[u8] {
        unsafe {
            core::slice::from_raw_parts(
                self.pixels.as_ptr() as *const u8,
                self.pixels.len() * 4,
            )
        }
    }
}

